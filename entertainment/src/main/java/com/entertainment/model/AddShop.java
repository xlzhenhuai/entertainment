package com.entertainment.model;

public class AddShop {
	
	private Shop Shop;
	
	private ShopDetails ShopDetails;
	
	private ShopPic ShopPic;

	public Shop getShop() {
		return Shop;
	}

	public void setShop(Shop shop) {
		Shop = shop;
	}

	public ShopDetails getShopDetails() {
		return ShopDetails;
	}

	public void setShopDetails(ShopDetails shopDetails) {
		ShopDetails = shopDetails;
	}

	public ShopPic getShopPic() {
		return ShopPic;
	}

	public void setShopPic(ShopPic shopPic) {
		ShopPic = shopPic;
	}
	
	

}
